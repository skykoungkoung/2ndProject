package cnav.main.dto;

public class BusinessDTO {

	private String code;
	private String bizName;
	private String bizCEO;
	private String bizNumber;
	private String bizTel;
	private String bizEmail;
	private String userId;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getBizName() {
		return bizName;
	}
	public void setBizName(String bizName) {
		this.bizName = bizName;
	}
	public String getBizCEO() {
		return bizCEO;
	}
	public void setBizCEO(String bizCEO) {
		this.bizCEO = bizCEO;
	}
	public String getBizNumber() {
		return bizNumber;
	}
	public void setBizNumber(String bizNumber) {
		this.bizNumber = bizNumber;
	}
	public String getBizTel() {
		return bizTel;
	}
	public void setBizTel(String bizTel) {
		this.bizTel = bizTel;
	}
	public String getBizEmail() {
		return bizEmail;
	}
	public void setBizEmail(String bizEmail) {
		this.bizEmail = bizEmail;
	}
	
	
}
